function updateBranding(branding) {
  const logo = branding.logoUrl || "/assets/dog-mitra-logo.png";
  const brandImage = document.querySelector(".brand-mark img");
  if (brandImage) brandImage.src = logo;
  const favicon = document.querySelector('link[rel="icon"]');
  if (favicon) favicon.href = logo;
  const appleTouch = document.querySelector('link[rel="apple-touch-icon"]');
  if (appleTouch) appleTouch.href = logo;
}

function updateMediaSlots(site) {
  const media = site.media || {};
  document.querySelectorAll("[data-media-slot]").forEach((node) => {
    const key = node.dataset.mediaSlot;
    const value = media[key];
    if (!value) return;
    if (node.tagName === "IMG") node.src = value;
    else node.style.backgroundImage = `url("${value}")`;
  });
}

function updateSocialLinks(links) {
  const container = document.querySelector("#socialLinks");
  if (!container) return;
  container.innerHTML = "";
  const socialIcons = {
    instagram: "IG",
    facebook: "FB",
    youtube: "YT",
    linkedin: "IN",
    googleBusiness: "GB",
  };
  Object.entries(links)
    .filter(([, value]) => Boolean(value))
    .forEach(([key, value]) => {
      const link = document.createElement("a");
      link.href = value;
      link.target = "_blank";
      link.rel = "noreferrer";
      link.textContent = socialIcons[key] || key;
      link.setAttribute("aria-label", key);
      container.appendChild(link);
    });
}

