const adminModal = document.querySelector("#adminModal");
const adminLoginButton = document.querySelector("#adminLoginButton");
const adminOpenButtons = document.querySelectorAll(".admin-open");
const closeAdmin = document.querySelector("#closeAdmin");
const adminLoginForm = document.querySelector("#adminLoginForm");
const upiCheckout = document.querySelector("#upiCheckout");
const adminToolButtons = document.querySelectorAll("[data-admin-section]");

if (adminLoginButton && adminModal) {
  adminLoginButton.addEventListener("click", function() {
    adminModal.classList.add("open");
    adminModal.setAttribute("aria-hidden", "false");
  });
}

adminOpenButtons.forEach(function(button) {
  button.addEventListener("click", function() {
    if (!adminModal) return;
    adminModal.classList.add("open");
    adminModal.setAttribute("aria-hidden", "false");
  });
});

if (closeAdmin && adminModal) {
  closeAdmin.addEventListener("click", function() {
    adminModal.classList.remove("open");
    adminModal.setAttribute("aria-hidden", "true");
  });
  adminModal.addEventListener("click", function(event) {
    if (event.target === adminModal) closeAdmin.click();
  });
}

if (adminLoginForm) {
  adminLoginForm.addEventListener("submit", function(event) {
    event.preventDefault();
    const formData = new FormData(adminLoginForm);
    fetch(`${apiBase}/auth/login`, {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify({
        identifier: formData.get("admin"),
        password: formData.get("password"),
      }),
    })
      .then(async (response) => {
        const data = await response.json().catch(() => ({}));
        if (!response.ok) throw new Error(data.message || "Login failed");
        setAuthTokens(data);
        if (adminStatus) adminStatus.textContent = "Login successful. Settings editor unlocked.";
        showAdminSection("settings");
      })
      .catch((error) => {
        if (adminStatus) adminStatus.textContent = error.message;
      });
  });
}

adminToolButtons.forEach(function(button) {
  button.addEventListener("click", function() {
    showAdminSection(button.dataset.adminSection);
  });
});

if (settingsForm) {
  settingsForm.addEventListener("submit", async function(event) {
    event.preventDefault();
    if (settingsStatus) settingsStatus.textContent = "Saving...";
    try {
      await saveSettings();
      if (settingsStatus) settingsStatus.textContent = "Settings saved.";
    } catch (error) {
      if (settingsStatus) settingsStatus.textContent = error.message;
    }
  });
}

